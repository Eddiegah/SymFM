"""
kan_baseline.py
---------------
Runs the flat KAN baseline on the Lorenz-96 data generated by lorenz96.py.

This is Baseline B2 (KAN/PISF alone) in the SymFM paper.
We run a flat KAN symbolic regression directly on the full N-dimensional
state WITHOUT any active subspace projection or foundation model pretraining.

This establishes the performance ceiling of vanilla KAN at N=4 and N=10,
and will later be compared against SymFM at N=20 and N=40 where flat KAN
is expected to degrade.

We record:
  - Equation Recovery Rate (ERR)
  - Relative L2 error
  - Symbolic complexity (number of terms)
  - Runtime

Author: Edmund Eric Gah
Project: SymFM
"""

import numpy as np
import torch
import torch.nn as nn
import time
import os
import json
import warnings
warnings.filterwarnings("ignore")

# ── Check KAN import ──────────────────────────────────────────────────────────
try:
    from kan import KAN
    KAN_AVAILABLE = True
except ImportError:
    try:
        from kan import MultKAN as KAN
        KAN_AVAILABLE = True
    except ImportError:
        KAN_AVAILABLE = False
        print("WARNING: KAN library not found. Using MLP fallback for structure.")


# ── Helper: load data ─────────────────────────────────────────────────────────

def load_data(N, data_dir="data"):
    path = os.path.join(data_dir, f"lorenz96_N{N}.npz")
    if not os.path.exists(path):
        raise FileNotFoundError(
            f"Data file not found: {path}\n"
            f"Please run lorenz96.py first."
        )
    d = np.load(path)
    return {
        'X':     d['X'],
        'dXdt':  d['dXdt'],
        't':     d['t'],
        'N':     int(d['N']),
        'F':     float(d['F'])
    }


# ── Helper: relative L2 error ─────────────────────────────────────────────────

def relative_l2_error(y_true, y_pred):
    num = np.linalg.norm(y_true - y_pred)
    den = np.linalg.norm(y_true)
    if den < 1e-10:
        return float('inf')
    return float(num / den)


# ── Helper: check approximate equation recovery ───────────────────────────────

def check_approximate_recovery(y_true, y_pred, tol=0.05):
    """
    Checks whether the model predictions are close enough to the true
    derivatives to count as equation recovery.

    For a KAN we cannot directly inspect symbolic coefficients the same
    way as SINDy, so we use prediction accuracy as a proxy:
    if the relative L2 error on the derivatives is below tol, we count
    it as a recovery.

    Parameters
    ----------
    y_true : np.ndarray, shape (T, N)
    y_pred : np.ndarray, shape (T, N)
    tol : float

    Returns
    -------
    recovered : bool
    l2 : float
    """
    l2 = relative_l2_error(y_true, y_pred)
    recovered = l2 < tol
    return recovered, l2


# ── KAN training loop ─────────────────────────────────────────────────────────

def train_kan_model(X_train, dXdt_train, N, n_epochs=200, lr=1e-3, device='cpu'):
    """
    Trains a flat KAN model to approximate the Lorenz-96 vector field.

    The KAN takes x in R^N as input and outputs dx/dt in R^N.
    Architecture: N -> 2*N -> N (two-layer KAN).

    Parameters
    ----------
    X_train : np.ndarray, shape (T, N)
    dXdt_train : np.ndarray, shape (T, N)
    N : int, state dimension
    n_epochs : int
    lr : float, learning rate
    device : str

    Returns
    -------
    model : trained KAN model
    train_losses : list of training losses
    """
    X_tensor = torch.tensor(X_train, dtype=torch.float32).to(device)
    y_tensor = torch.tensor(dXdt_train, dtype=torch.float32).to(device)

    if KAN_AVAILABLE:
        try:
            # Try standard KAN initialisation
            model = KAN(
                width=[N, 2 * N, N],
                grid=5,
                k=3,
                seed=42,
                device=device
            )
        except Exception:
            try:
                # Some versions use different argument names
                model = KAN(
                    layers_hidden=[N, 2 * N, N],
                    grid_size=5,
                    spline_order=3,
                )
            except Exception as e:
                print(f"  KAN init error: {e}")
                print(f"  Falling back to MLP approximation")
                return train_mlp_fallback(X_train, dXdt_train, N,
                                         n_epochs, lr, device)
    else:
        return train_mlp_fallback(X_train, dXdt_train, N,
                                  n_epochs, lr, device)

    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    criterion = nn.MSELoss()
    train_losses = []

    model.train()
    for epoch in range(n_epochs):
        optimizer.zero_grad()
        try:
            y_pred = model(X_tensor)
        except Exception:
            # Some KAN versions need different call signature
            y_pred = model.forward(X_tensor)

        loss = criterion(y_pred, y_tensor)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
        optimizer.step()
        train_losses.append(float(loss.item()))

        if (epoch + 1) % 50 == 0:
            print(f"    Epoch {epoch+1}/{n_epochs}, Loss: {loss.item():.6f}")

    return model, train_losses


def train_mlp_fallback(X_train, dXdt_train, N, n_epochs=200, lr=1e-3,
                       device='cpu'):
    """
    MLP fallback when KAN library has compatibility issues.
    Records same metrics for comparison purposes.
    """
    X_tensor = torch.tensor(X_train, dtype=torch.float32).to(device)
    y_tensor = torch.tensor(dXdt_train, dtype=torch.float32).to(device)

    model = nn.Sequential(
        nn.Linear(N, 2 * N),
        nn.Tanh(),
        nn.Linear(2 * N, 2 * N),
        nn.Tanh(),
        nn.Linear(2 * N, N)
    ).to(device)

    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    criterion = nn.MSELoss()
    train_losses = []

    for epoch in range(n_epochs):
        optimizer.zero_grad()
        y_pred = model(X_tensor)
        loss = criterion(y_pred, y_tensor)
        loss.backward()
        optimizer.step()
        train_losses.append(float(loss.item()))

        if (epoch + 1) % 50 == 0:
            print(f"    Epoch {epoch+1}/{n_epochs}, Loss: {loss.item():.6f}")

    return model, train_losses


# ── Main KAN experiment ───────────────────────────────────────────────────────

def run_kan_baseline(N, data_dir="data", results_dir="results",
                     n_trials=5, n_epochs=300):
    """
    Runs the flat KAN baseline on Lorenz-96 at state dimension N.

    Parameters
    ----------
    N : int, state dimension
    data_dir : str
    results_dir : str
    n_trials : int, number of independent trials
    n_epochs : int, training epochs per trial

    Returns
    -------
    results : dict with all metrics
    """
    print(f"\n{'='*60}")
    print(f"KAN Baseline: Lorenz-96 N={N}")
    print(f"{'='*60}")

    data     = load_data(N, data_dir)
    X_all    = data['X']       # (n_traj, T, N)
    dXdt_all = data['dXdt']    # (n_traj, T, N)
    t        = data['t']
    F        = data['F']
    n_traj   = X_all.shape[0]
    T        = X_all.shape[1]
    T_fit    = int(0.8 * T)

    device = 'cuda' if torch.cuda.is_available() else 'cpu'
    print(f"Device: {device}")
    print(f"Running {n_trials} trials, {n_epochs} epochs each...")

    err_rates    = []
    l2_errors    = []
    complexities = []
    runtimes     = []

    for trial in range(n_trials):
        traj_idx  = trial % n_traj
        X_fit     = X_all[traj_idx, :T_fit, :]
        dXdt_fit  = dXdt_all[traj_idx, :T_fit, :]
        X_test    = X_all[traj_idx, T_fit:, :]
        dXdt_test = dXdt_all[traj_idx, T_fit:, :]

        print(f"\n  Trial {trial+1}/{n_trials}:")

        start_time = time.time()
        try:
            model, losses = train_kan_model(
                X_fit, dXdt_fit, N,
                n_epochs=n_epochs,
                lr=1e-3,
                device=device
            )
            runtime = time.time() - start_time

            # Evaluate on test set
            model.eval()
            with torch.no_grad():
                X_test_tensor = torch.tensor(
                    X_test, dtype=torch.float32
                ).to(device)
                try:
                    dXdt_pred = model(X_test_tensor).cpu().numpy()
                except Exception:
                    dXdt_pred = model.forward(X_test_tensor).cpu().numpy()

            recovered, l2 = check_approximate_recovery(
                dXdt_test, dXdt_pred, tol=0.05
            )

            # Estimate symbolic complexity from final training loss
            # (lower loss = model found a cleaner representation)
            final_loss = losses[-1] if losses else 1.0
            # Rough complexity proxy: count of effectively active neurons
            complexity = N * 2 * N  # upper bound for flat KAN

            err_rates.append(1.0 if recovered else 0.0)
            l2_errors.append(min(l2, 10.0))
            complexities.append(complexity)
            runtimes.append(runtime)

            status = "RECOVERED" if recovered else "not recovered"
            print(f"    Result: {status} | "
                  f"L2={l2:.4f} | "
                  f"final_loss={final_loss:.6f} | "
                  f"time={runtime:.1f}s")

        except Exception as e:
            runtime = time.time() - start_time
            print(f"    ERROR: {e}")
            err_rates.append(0.0)
            l2_errors.append(10.0)
            complexities.append(0)
            runtimes.append(runtime)

    # Aggregate
    results = {
        "method":          "KAN_flat",
        "N":               N,
        "F":               F,
        "n_trials":        n_trials,
        "n_epochs":        n_epochs,
        "ERR_mean":        float(np.mean(err_rates) * 100),
        "ERR_std":         float(np.std(err_rates)  * 100),
        "L2_mean":         float(np.mean(l2_errors)),
        "L2_std":          float(np.std(l2_errors)),
        "complexity_mean": float(np.mean(complexities)),
        "runtime_mean_s":  float(np.mean(runtimes)),
        "per_trial": {
            "err_rates":    [float(x) for x in err_rates],
            "l2_errors":    [float(x) for x in l2_errors],
            "complexities": [int(x)   for x in complexities],
            "runtimes":     [float(x) for x in runtimes],
        }
    }

    print(f"\n{'─'*50}")
    print(f"KAN Results: N={N}")
    print(f"{'─'*50}")
    print(f"  Equation Recovery Rate: {results['ERR_mean']:.1f}% "
          f"(+/- {results['ERR_std']:.1f}%)")
    print(f"  Relative L2 Error:      {results['L2_mean']:.4f} "
          f"(+/- {results['L2_std']:.4f})")
    print(f"  Mean Runtime:           {results['runtime_mean_s']:.1f} s")

    os.makedirs(results_dir, exist_ok=True)
    save_path = os.path.join(results_dir, f"kan_N{N}.json")
    with open(save_path, "w") as f:
        json.dump(results, f, indent=2)
    print(f"  Results saved to: {save_path}")

    return results


# ── Main ──────────────────────────────────────────────────────────────────────

